#operadores entre numeros (int e float)

a = 2.5
b = 10

print(a + b) #soma
print(a - b) #subtracao
print(a * b) #multiplicacao
print(a / b) #divisao
print(a % b) #modulo (resto da divisao) 
print(a ** b) #exponenciacao

